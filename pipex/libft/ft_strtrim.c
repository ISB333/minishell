#include "libft.h"

char	*ft_strtrim(char *s1, char const *set)
{
	int	len;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len > 0 && ft_strrchr(set, s1[len - 1]) && len--)
		if (len == 0)
			return (ft_strdup(""));
	return (ft_substr(s1, 0, len));
}
