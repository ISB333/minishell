#include "minishell.h"

int	is_del(char c)
{
	if (c == ' ' || c == '\t' || c == '\n')
		return (1);
	return (0);
}

int	is_sh_ope(char *s, int i)
{
	if (s[i] == '|')
		return (1);
	if ((s[i] == '<' && s[i + 1] != '<') || (s[i] == '>' && s[i + 1] != '>'))
		return (1);
	if ((s[i] == '<' && s[i + 1] == '<') || (s[i] == '>' && s[i + 1] == '>'))
		return (2);
	return (0);
}